    private void jtextLegajoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtextLegajoFocusLost
       
        try{   //---Si no Ingreso un numero que maneje el error---
            
            int valorJText = Integer.parseInt(jtextLegajo.getText());
            
            //---Chequeo si el legajo no esta almacenado con anteiorirdad---
            if (chequearSiExisteEseLegajoDeAlumno(valorJText)){
                
                JOptionPane.showMessageDialog(this, "El Id de Legajo: " + jtextLegajo.getText() + " ya esta asignado\n\n       Ingrese otro diferente por favor!\n");
                jtextLegajo.setText("");
                jtextLegajo.requestFocus();
                
            }

        }catch(NumberFormatException e){

            if (!jtextLegajo.getText().equals("")){                             //<---Si el jtextLegajo esta vacio el campo, que No este disparando un JoptionPanee molesto al usuario
                JOptionPane.showMessageDialog(this, "El campo 'Codigo de Materias:' solo admite Numeros");
                jtextLegajo.requestFocus();
               
                
            }
        }
    }//GEN-LAST:event_jtextLegajoFocusLost

