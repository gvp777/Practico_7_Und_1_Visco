    private boolean chequearSiExisteEseLegajoDeAlumno(int legajoDeAlumnoParam){            //<---Este metodo lo llamo poara pasarle un Legajo de Alumno y chequear si el mismo ya no esta almacenado
    
        ArrayList<Alumno> alumnos  = Menu.getAlumnos();
        boolean resultado = false;
        
        for(Alumno alumnoIt: alumnos){
        
            resultado = (legajoDeAlumnoParam == alumnoIt.getLegajo());
        }
                
        return resultado;
    }
