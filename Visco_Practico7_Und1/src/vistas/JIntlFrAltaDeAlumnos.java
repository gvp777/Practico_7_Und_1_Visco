    private void registrarAlumno(){
      
        //---Chequeo, antes, si hay campos sin completar---
        if (jtextLegajo.getText().equals("") ||                                  
            jtextApellido.getText().equals("") ||
            jtextNombre.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "No pueden quedar campos sin completar!");  
  
        }else {
            
            //---Recupero los datops de los testField, le saco los espacios de los costados y los guardo en mayuscula---
            int idLegajo = Integer.parseInt(jtextLegajo.getText());                       
            String apellidoAlumno = jtextApellido.getText().trim().toUpperCase();
            String nombreAlumno = jtextNombre.getText().trim().toUpperCase();
            
            
            //---Agrego un/a alumno/a en el arreglo de Alumnos que esta en en Menu---
            Menu.getAlumnos().add(new Alumno(idLegajo, apellidoAlumno, nombreAlumno));
 
            //---Aviso al usuario---
            JOptionPane.showMessageDialog(this, "Registro guardado Exitosamente!!!");
            jtextLegajo.requestFocus();
            limpiarCampos();
        
        }
    
    }
    
